package ch02_variable;

public class VariableType {

	public static void main(String[] args) {
		
		// 변수의 타입
		// 정수 타입
		byte byteVar = 1;
		short shortVar = 2;
		int intVar = 3;
		long longVar = 30000000000L;
		
		// 정수이면서 문자
		char charVar = 40032; // '가'와 같음
		
		// 소수 타입
		float floatVar = 3.14f;
		double doubleVar = 3.14;
		
		// 불리언 타입
		boolean bollVar = false;
		
		// 문자열 타입
		String strToday = "오늘은 1월 22일";
		
		System.out.println(strToday);

		// Ctrl + d 라인 삭제
		// Ctrl + Alt + 아래방향키 라인복사
		// 정렬 영역 선택 후 Ctrl + Shift + f
		// Alt + 위, 아래 라인이동
	}

}
